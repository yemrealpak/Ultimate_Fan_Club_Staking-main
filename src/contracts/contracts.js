export const TokenAddress = () => {
  const { chain } = useNetwork();
  let chainId = chain?.id;

  if (chainId === 56) {
    return "0x71B0E1fe13258C552e5F962d9D3deCF8a24d6583";
  }
  if (chainId === 97) {
    return "0xd0E43Ba61a18fA5574732627A33963bb9fAeABB5";
  }
  if (chainId == undefined) {
    return "0x71B0E1fe13258C552e5F962d9D3deCF8a24d6583";
  }
};

